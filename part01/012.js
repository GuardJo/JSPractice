// 조건문 - switch

var subject = '자바스크립트';
switch (subject) {
    case 'C언어':
        console.log("C언어 초보");
        break;
    case '자바스크립트':
        console.log("자바스크립트 초보");
        break;
    case '파이썬' :
        console.log("파이썬 초보");
        break;
    default:
        console.log("초보가 아닙니다.");
        break;
}